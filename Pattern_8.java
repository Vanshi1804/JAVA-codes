public class Pattern_8 {
    public static void main(String[] args) {

        int n = 2;

        for(int i=0;i<5;i++)
        {
            for(int j=0;j<5;j++)
            {
                System.out.print(" "+n);
                n += 2;
            }
            System.out.println();
        }

    }
}
